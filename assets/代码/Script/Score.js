cc.Class({
    extends: cc.Component,

    properties: {
        scoreDisplay: {
            default: null,
            type: cc.Label
        },
    },

    // LIFE-CYCLE CALLBACKS:

    onLoad () {
        // 初始化计分
        this.score = 0;
    },



    update (dt) {
        this.score += dt*1000;
        this.scoreDisplay.string = 'Score: ' + this.score.toString();
    },
});

